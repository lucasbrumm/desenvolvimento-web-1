class Product {
  constructor(
    id,
    title,
    description,
    price,
    brand,
    category,
    photos,
    thumbnail
  ) {
    this.id = id
    this.title = title
    this.description = description
    this.price = price
    this.brand = brand
    this.category = category
    this.photos = photos
    this.thumbnail = thumbnail
  }
}
